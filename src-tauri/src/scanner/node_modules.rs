use std::fs;
use std::path::{Path, PathBuf};

use uuid::Uuid;

use crate::models::{Category, RiskLevel, ScanItem};
use crate::scanner::util::{
    dir_size, expand_path, file_age_days, file_mtime_unix, is_excluded, is_never_touch_scan_path,
};

fn read_package_name_version(package_json: &Path) -> (Option<String>, Option<String>) {
    let Ok(data) = fs::read_to_string(package_json) else {
        return (None, None);
    };
    let Ok(v) = serde_json::from_str::<serde_json::Value>(&data) else {
        return (None, None);
    };
    (
        v.get("name").and_then(|x| x.as_str()).map(|s| s.to_string()),
        v.get("version").and_then(|x| x.as_str()).map(|s| s.to_string()),
    )
}

pub fn scan_node_modules(
    home: &Path,
    search_paths: &[String],
    threshold_days: u32,
    exclude_paths: &[String],
) -> Result<Vec<ScanItem>, String> {
    let roots: Vec<PathBuf> = search_paths
        .iter()
        .filter(|s| !s.trim().is_empty())
        .map(|s| expand_path(s, home))
        .collect();

    let defaults = [
        home.join("Developer"),
        home.join("Projects"),
        home.join("Desktop"),
        home.join("Documents"),
    ];

    let roots = if roots.is_empty() {
        defaults.into_iter().collect::<Vec<_>>()
    } else {
        roots
    };

    let mut items = Vec::new();

    for root in roots {
        if !root.exists() {
            continue;
        }
        let mut it = walkdir::WalkDir::new(&root).follow_links(false).into_iter();
        loop {
            let entry = match it.next() {
                None => break,
                Some(Ok(e)) => e,
                Some(Err(e)) => return Err(format!("walk {root:?}: {e}")),
            };

            let path = entry.path();
            if is_never_touch_scan_path(home, path) || is_excluded(path, exclude_paths, home) {
                if entry.file_type().is_dir() {
                    it.skip_current_dir();
                }
                continue;
            }

            let name = entry.file_name().to_string_lossy();
            if entry.depth() > 0 && name.starts_with('.') {
                if entry.file_type().is_dir() {
                    it.skip_current_dir();
                }
                continue;
            }

            if entry.file_type().is_dir() && name == "node_modules" {
                let age = file_age_days(path).unwrap_or(0);
                if age >= threshold_days as i64 {
                    let parent = path.parent().unwrap_or(path);
                    let pkg = parent.join("package.json");
                    let (pname, pver) = read_package_name_version(&pkg);
                    let item_name = match (pname, pver) {
                        (Some(n), Some(v)) => format!("{n}@{v}"),
                        (Some(n), None) => n,
                        _ => parent
                            .file_name()
                            .map(|s| s.to_string_lossy().to_string())
                            .unwrap_or_else(|| "node_modules".to_string()),
                    };

                    let size = dir_size(path).unwrap_or(0);
                    let mt = file_mtime_unix(path).ok();

                    let risk = if age > 730 {
                        RiskLevel::Recommended
                    } else {
                        RiskLevel::Caution
                    };

                    items.push(ScanItem {
                        id: Uuid::new_v4().to_string(),
                        category: Category::NodeModules,
                        name: item_name,
                        path: path.display().to_string(),
                        size_bytes: size,
                        last_modified: mt,
                        last_accessed: None,
                        risk_level: risk,
                        description: format!(
                            "node_modules older than {} days (last activity ~{age} days).",
                            threshold_days
                        ),
                        related_app: None,
                        is_deletable: true,
                    });
                }
                it.skip_current_dir();
            }
        }
    }

    Ok(items)
}
